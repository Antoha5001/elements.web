                        }

                        el.style.height = '';

                        el.classList.remove('show');
                    }
                    // console.log(el);
                });
                if (this.parentNode.style.height === 'auto'){
                    this.parentNode.style.height = '';
                } else{

                    this.parentNode.style.height = 'auto';
                }
                this.parentNode.classList.toggle('show');

                // console.log(this);
            });
        });

        function menuShow() {
            if(!show){

                show = true;
                MENU_TOP_BUTTON.classList.add('show');
